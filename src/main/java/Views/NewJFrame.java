    private void listar() {
        try {
            modelo.setRowCount(0); //limpiar la tabla
            //limpiamos la tabla
            objeto.leerDatos();
            
             
            for(Estudiante est: objeto.array){
                Object[] fila = {
                    est.getNombre(), est.getProyecto1(), est.getExamen1(), est.getExamen2(), est.getProyecto2()
                };
                modelo.addRow(fila);
            }
            
            
        } catch (Exception e) {
        }
    }
